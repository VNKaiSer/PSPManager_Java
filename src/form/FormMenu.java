    private void jButton2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton2MouseClicked
        // TODO add your handling code here:
        if (jTextField1.getText().length() == 0 || jTextField2.getText().length() == 0) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập đầy đủ dữ liệu");
            jTextField1.requestFocus();
        } else {
            String ten = jTextField1.getText();
            String gia = jTextField2.getText();
            int stt = model.getRowCount() + 1;
            String ma = "MN";
            if (stt < 9) {
                ma+= "00"+stt;
            } else if(stt <99){
                ma+= "0"+stt;
            }else{
                ma+=stt;
            }
            
            Menu tmp = new Menu(ma, ten, Float.parseFloat(gia));
            model.addRow(new Object[]{stt, tmp.getMenuID(), tmp.getName(), tmp.getPrice()});
            try {
                // Thêm vào data base
                menu_DAO.addNewMenu(tmp);
            } catch (SQLException ex) {
                Logger.getLogger(FormMenu.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jButton2MouseClicked

