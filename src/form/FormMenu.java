    private void jButton3MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton3MouseClicked
        // TODO add your handling code here:
        int index = jTable1.getSelectedRow();
        if (index == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn dòng cần xóa");
            return;
        }else{
            int acp = JOptionPane.showConfirmDialog(null,"Cảnh báo","Có Chắc Chắn Muốn Xóa Không", JOptionPane.YES_NO_OPTION);
            if(acp == JOptionPane.YES_OPTION){ // xóa
                
                try {
                    
                    menu_DAO.deleteMenu(model.getValueAt(index, 1).toString());
                    model.removeRow(index);
                } catch (SQLException ex) {
                    Logger.getLogger(FormMenu.class.getName()).log(Level.SEVERE, null, ex);
                }
            } else{
                return;
            }
        }
    }//GEN-LAST:event_jButton3MouseClicked

    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        // TODO add your handling code here:
         int index = jTable1.getSelectedRow();
        if (index == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn dòng cần xóa");
            return;
        } else{
           
            float price = Float.parseFloat(jTextField2.getText().toString());
            String ma = model.getValueAt(index, 1).toString();
             try {
                 model.setValueAt(price, index, 3);
                 menu_DAO.updatePrice(ma, price);
             } catch (SQLException ex) {
                 Logger.getLogger(FormMenu.class.getName()).log(Level.SEVERE, null, ex);
             }
        }
    }//GEN-LAST:event_jButton1MouseClicked

    private void addDataToTable() throws SQLException {
        ArrayList<Menu> list = menu_DAO.getDanhSachMenu();
        int i = 1;
        for (Menu it : list) {
            model.addRow(new Object[]{i++, it.getMenuID(), it.getName(), it.getPrice()});
        }
    }

